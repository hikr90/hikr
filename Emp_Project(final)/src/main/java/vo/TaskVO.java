package vo;

public class TaskVO {
	private int task_idx;
	private int emp_idx;
	private String task;

	public int getTask_idx() {
		return task_idx;
	}
	public void setTask_idx(int task_idx) {
		this.task_idx = task_idx;
	}
	public int getEmp_idx() {
		return emp_idx;
	}
	public void setEmp_idx(int emp_idx) {
		this.emp_idx = emp_idx;
	}
	public String getTask() {
		return task;
	}
	public void setTask(String task) {
		this.task = task;
	}
	
	
	
}
